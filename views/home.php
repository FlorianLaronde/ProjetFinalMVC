                        <h3 class="text-right">2. Les comètes</h3>
                        <p class="text-right">Ce sont des corps orbitant autour du soleil à des milliards de kilomètres et qui déséquilibrés par des champs 
                        magnétiques ou des effets de gravitation des étoiles proches, plongent vers le Soleil et deviennent visibles.</p>
                        <p>Il en existe des milliards, elles peuvent apparaître en n'importe quel point du ciel.</p>
                        <p>En 1995, 878 comètes ont été cataloguées et leur orbite, au moins grossièrement, calculée. De celles-ci, 184 sont des comètes
                        périodiques  (période orbitale de moins de 200 ans). Certaines des autres sont sans doute aussi périodiques mais leurs orbites n'ont 
                        pas été calculées avec assez de précision pour le savoir.</p>
                        <p>En 2011, 3 000 comètes ont été cataloguées. En 2019 on en compte 3 563.</p>
