                        <h3 class="text-right">2. Les planètes gazeuses</h3>
                        <p class="text-right">Une planète géante gazeuse, souvent abrégée en géante gazeuse est une planète géante composée
                        essentiellement de gaz légers, c’est-à-dire d’hydrogène et d’hélium.</p>
                        <p>Le Système solaire possède deux planètes géantes gazeuses : Jupiter et Saturne.</p>
                        <p>Pour catégoriser les 4 dernières planètes du système solaire, le terme "planète géante" a longtemps été utilisé. Mais dans les
                        années 1990, il devint clair qu’Uranus et Neptune n'étaient pas composées des mêmes éléments que Jupiter et Saturne. En effet, Uranus 
                        et Neptune contiennent des éléments plus lourds tels que l’eau, le méthane et l’ammoniac, que l’on appelle en planétologie "des glaces". 
                        C'est pourquoi deux types de planètes géantes ont été créés.</p>
